package com.Assignment;

public class OddOrEven {
    public static void main(String[] args) {
        for(int i=0; i<=100 ; i++){
            if(i%2 == 0){
                System.out.println("Even Number" + i);
            }
            else {
                System.out.println("Odd number " + i);
            }
        }
    }
}
